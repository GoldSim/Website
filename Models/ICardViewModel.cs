namespace GoldSim.Web.Models {

  /*============================================================================================================================
  | INTERFACE: CARD VIEW MODEL
  \---------------------------------------------------------------------------------------------------------------------------*/
  /// <summary>
  ///   Ensures that a <see cref="TopicViewModel"/> meets the base requirements for being treated as a card.
  /// </summary>
  public interface ICardViewModel {

    /*==========================================================================================================================
    | THUMBNAIL IMAGE
    \-------------------------------------------------------------------------------------------------------------------------*/
    /// <summary>
    ///   Provides a thumbnail image to display in the card format.
    /// </summary>
    string ThumbnailImage { get; }

    /*==========================================================================================================================
    | WEB PATH
    \-------------------------------------------------------------------------------------------------------------------------*/
    /// <summary>
    ///   Provides the <see cref="OnTopic.Topic.GetWebPath()"/>, which represents the public-facing URL of the topic.
    /// </summary>
    string WebPath { get; }

    /*==========================================================================================================================
    | TITLE
    \-------------------------------------------------------------------------------------------------------------------------*/
    /// <summary>
    ///   Provides a friendly title to use for the card.
    /// </summary>
    string Title { get; }

    /*==========================================================================================================================
    | SHORT TITLE
    \-------------------------------------------------------------------------------------------------------------------------*/
    /// <summary>
    ///   Optionally overrides the <see cref="Title"/> with a shorter title more appropriate for display on the card format.
    /// </summary>
    string ShortTitle { get; }

  } // Interface
} // Namespace